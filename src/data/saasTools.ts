// Helper: get tool by slug
export function getToolBySlug(slug: string): SaasTool | undefined {
  return saasTools.find((t) => t.slug === slug);
}

// Helper: get tools by category
export function getToolsByCategory(category: ToolCategory): SaasTool[] {
  return saasTools.filter((t) => t.category === category);
}

// Helper: get alternatives for a tool
export function getAlternatives(tool: SaasTool): SaasTool[] {
  return tool.alternativeSlugs
    .map((s) => getToolBySlug(s))
    .filter(Boolean) as SaasTool[];
}

// Helper: get tools for a best-of list
export function getBestOfTools(listSlug: string): SaasTool[] {
  const list = BEST_OF_LISTS[listSlug];
  if (!list) return [];
  return list.toolSlugs
    .map((s) => getToolBySlug(s))
    .filter(Boolean) as SaasTool[];
}
